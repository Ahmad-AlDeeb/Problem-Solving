void fast() {
    ll n; cin >> n;

    set<ll> s1,s2;
    bool cool{true};
    for(ll i{}; i<n; i++) {
        ll x; cin >> x;
        if(!s1.count(x)) s1.insert(x);
        else if(!s2.count(x)) s2.insert(x);
        else cool = false;
    }

    if(!cool) cout << "NO";
    else {
        vll v2; for(auto num:s2) v2.pb(num);
        sort(all(v2),greater<>());

        cout << "YES\n";
        cout << s1.size() << endl;
        for(auto num:s1) cout << num << " ";
        cout << endl;
        cout << v2.size() << endl;
        for(auto num:v2) cout << num << " ";
    }
}
